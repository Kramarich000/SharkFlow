export function getWarnings(info) {
  const messages = [];

  if (info.isIncognito && browserWarningsMap.isIncognito) {
    messages.push(browserWarningsMap.isIncognito);
  }

  if (info.browserName) {
    const key = `browserName:${info.browserName}`;
    if (browserWarningsMap[key]) {
      messages.push(browserWarningsMap[key]);
    }
  }

  if (info.deviceType) {
    const key = `deviceType:${info.deviceType}`;
    if (browserWarningsMap[key]) {
      messages.push(browserWarningsMap[key]);
    }
  }

  if (messages.length === 0 && browserWarningsMap.default) {
    messages.push(browserWarningsMap.default);
  }

  return messages;
}
